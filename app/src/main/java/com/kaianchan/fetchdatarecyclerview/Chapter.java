package com.kaianchan.fetchdatarecyclerview;

public class Chapter {

    private String image;
    private String title;

    // Constructor
    public Chapter(String image, String title) {
        this.image = image;
        this.title = title;
    }

    // Getter

    public String getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }
}
